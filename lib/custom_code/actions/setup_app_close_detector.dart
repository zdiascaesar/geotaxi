import 'package:cloud_firestore/cloud_firestore.dart';

Future<void> setupAppCloseDetector() async {
  WidgetsBinding.instance.addObserver(_AppCloseObserver());
}

class _AppCloseObserver with WidgetsBindingObserver {
  @override
  void didChangeAppLifecycleState(AppLifecycleState state) {
    if (state == AppLifecycleState.detached) {
      _updateReadyToWorkOnClose();
    }
  }

  void _updateReadyToWorkOnClose() {
    final userId = FirebaseAuth.instance.currentUser?.uid;
    if (userId == null) return;

    final userRef = FirebaseFirestore.instance.collection('users').doc(userId);
    userRef.update({'ready_to_work': false}).catchError((error) {
      print('Error updating ready_to_work on app close: $error');
    });
  }
}
